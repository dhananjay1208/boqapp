-- Backfill: every existing site belongs to Cogneta (we own all pre-existing data).
UPDATE sites
   SET tenant_id = (SELECT id FROM tenants WHERE company_code = 'Cogneta')
 WHERE tenant_id IS NULL;

ALTER TABLE sites ALTER COLUMN tenant_id SET NOT NULL;
